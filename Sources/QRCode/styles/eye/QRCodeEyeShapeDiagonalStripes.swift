public extension QRCode.EyeShape {
	@objc(QRCodeEyeShapeDiagonalStripes) class DiagonalStripes: NSObject, QRCodeEyeShapeGenerator {
		@objc public static let Name = "diagonalStripes"
		@objc public static var Title: String { "Diagonal Stripes" }
		@objc public static func Create(_ settings: [String: Any]?) -> any QRCodeEyeShapeGenerator { DiagonalStripes() }
		@objc public func settings() -> [String: Any] { return [:] }
		@objc public func supportsSettingValue(forKey key: String) -> Bool { false }
		@objc public func setSettingValue(_ value: Any?, forKey key: String) -> Bool { false }
		@objc public func copyShape() -> any QRCodeEyeShapeGenerator { DiagonalStripes() }

		/// Reset the eye shape generator back to defaults
		@objc public func reset() { }

		public func eyePath() -> CGPath { eyeFrameOuter__ }
		public func eyeBackgroundPath() -> CGPath { eyeFrameBackground__ }

		private static let _defaultPupil = QRCode.PupilShape.DiagonalStripes()
		public func defaultPupil() -> any QRCodePupilShapeGenerator { Self._defaultPupil }
	}
}

